// JavaScript Hoisting refers to the process whereby 
// the interpreter appears to move the declaration of functions, variables or classes 
// to the top of their scope, prior to execution of the code. 
// Hoisting allows functions to be safely used in code before they are declared.

// Hoisting 
// 1- Finds all of the variable and function declarations in the code
// 2- ‘Raises’ the variable names and function declarations to the top of their scope before code execution
// 3- Immediately initializes function declarations
// 4- Postpones handling initialization, or assignment of values, until the code is executed